#!/usr/bin/env node
/**
 * Build PWA static export for a specific product shell (desktop | mobile | web).
 * Used by Tauri beforeBuildCommand and CI Android lane.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { spawnSync } from "node:child_process";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { buildStaticShellManifest, resolveStaticShellSourceRevision, STATIC_SHELL_MANIFEST_FILE } from "./lib/static-shell-stale.mjs";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const rootVersion = JSON.parse(readFileSync(path.join(repoRoot, "package.json"), "utf8")).version ?? "dev";
const shellArg = (process.argv[2] ?? process.env.TAURI_SHELL_TARGET ?? "desktop").toLowerCase();

const shellEnv = (() => {
  if (shellArg === "mobile") {
    return {
      NEXT_PUBLIC_MOBILE_SHELL: "1",
      NEXT_PUBLIC_DESKTOP_SHELL: "0",
      NEXT_PUBLIC_OBSCUR_RADICAL_TRUTH: "0",
    };
  }
  if (shellArg === "web") {
    return { NEXT_PUBLIC_MOBILE_SHELL: "0", NEXT_PUBLIC_DESKTOP_SHELL: "0" };
  }
  return {
    NEXT_PUBLIC_DESKTOP_SHELL: "1",
    NEXT_PUBLIC_MOBILE_SHELL: "0",
    NEXT_PUBLIC_OBSCUR_RADICAL_TRUTH: "0",
    // Production desktop installers must use real relay transport — not experiment offline stub.
    NEXT_PUBLIC_OBSCUR_EXPERIMENT_ONLINE:
      process.env.NEXT_PUBLIC_OBSCUR_EXPERIMENT_ONLINE ?? "1",
  };
})();

const env = {
  ...process.env,
  ...shellEnv,
  TAURI_BUILD: "true",
  NEXT_PUBLIC_APP_VERSION: rootVersion,
};

console.log(`[build-pwa-shell] target=${shellArg} env=${JSON.stringify(shellEnv)}`);

if (shellArg === "desktop") {
  const sourceRevision = resolveStaticShellSourceRevision(repoRoot);
  const clientBuildStampPath = path.join(repoRoot, "apps", "pwa", "app", "shared", "client-build-stamp.ts");
  writeFileSync(
    clientBuildStampPath,
    `/** Auto-generated by scripts/build-pwa-shell.mjs — do not edit manually. */\nexport const CLIENT_BUILD_STAMP = ${JSON.stringify(sourceRevision.stamp)};\n`,
    "utf8",
  );
  console.log(`[build-pwa-shell] stamped CLIENT_BUILD_STAMP=${sourceRevision.stamp}`);
}

const result = spawnSync(
  "pnpm",
  ["-C", "apps/pwa", "run", "build:static"],
  { cwd: repoRoot, env, stdio: "inherit", shell: true },
);

if ((result.status ?? 1) === 0 && shellArg === "desktop") {
  const outDir = path.join(repoRoot, "apps", "pwa", "out");
  const sourceRevision = resolveStaticShellSourceRevision(repoRoot);
  const manifest = buildStaticShellManifest(env, {
    stamp: sourceRevision.stamp,
    newestPath: sourceRevision.relativeNewestPath,
    newestMtimeMs: sourceRevision.mtimeMs,
  });
  writeFileSync(
    path.join(outDir, STATIC_SHELL_MANIFEST_FILE),
    `${JSON.stringify(manifest, null, 2)}\n`,
    "utf8",
  );
  console.log(
    `[build-pwa-shell] wrote ${STATIC_SHELL_MANIFEST_FILE} (experimentOnline=${env.NEXT_PUBLIC_OBSCUR_EXPERIMENT_ONLINE === "1"}, stamp=${manifest.clientBuildStamp})`,
  );
}

process.exit(result.status ?? 1);
